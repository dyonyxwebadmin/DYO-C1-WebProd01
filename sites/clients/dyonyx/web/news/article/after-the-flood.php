              <p><strong>Houston, TX</strong> &mdash; Our community in Houston has been banding together the last few days as we've been experiencing the damaging effects of Hurricane Harvey. We have all been hearing and processing a lot of information coming our way, and we've put together a list of information that might be useful for anyone impacted by the storm. Please note: we hope this information is helpful, but it is not intended to be used as legal, medical or any professional advice (in other words, no liabilities, guarantees or warranties are being issued with this note).</p>


              <p>In case you missed it, FEMA held a news conference yesterday, urging everyone to register for an account with FEMA if you think you have any damage.</p> 

              <p>You can do this in 3 ways:</p>

              <ul>
                <li>Go online to DisasterAssistance.gov.</li>
                <li>Use the FEMA app for smartphones.</li>
                <li>call at 1-800-621-3362 (although you may find the phone lines are tied up)</li>
              </ul>


              <p>Additional information you may find useful: </p>

              <ol>
                <li><strong>Ensure physical safety</strong> - everything else can be replaced - you can’t.</li>

                <li>You are in a marathon now, not a sprint - <strong>everything will take much, much longer than you want it to.</strong> You will be dealing with the federal government (National Flood Insurance Program) and they move at their own pace....it is a process! </li>

                <li><strong>Take pictures and video</strong> - lots of pictures and video. Talk through your video's with the brand and model of the items that you have damaged to help with the claims process. Establish how high the water was inside and outside of your house. You need to prove how deep the water was as part of your flood claim. Use a yardstick or ruler on the outside of your house to establish the high water mark. </li>

                <p><strong>Take pictures of any damage you see,</strong> wet sheetrock, wet carpet, wet furniture, anything you want to claim - document. For contents, document individual items - each shirt, book, etc. needs to be enumerated and documented for the claim - if you say 20 books on your claim, you need a photograph where 20 books can be individually accounted for - be exact and over detailed. They will want to know when you bought the item, where you bought the item, and what you paid for it. Flood insurance Contents Coverage is on an Actual Cash Basis. Therefore, they will take the original cost of the item less the depreciation and that is what you will be paid for the loss on said item. camera.png</p>

                <li><strong>File your claim immediately</strong> - get in line for adjustors, etc.! </li>

                <li><strong>Flood insurance will not reimburse you for loss of use,</strong> so any hotel or lodging expenses will be out of pocket. There is not an option for "Loss of Use" on the Flood Policy like the Homeowner and Windstorm policies. </li>

                <li><strong>Save all receipts</strong> - all of them. </li>

                <li><strong>Consider ordering a POD or storage container</strong> as they will sell out fast. </li>

                <li><strong>As soon as the water recedes, start mitigating the damage.</strong> Shopvac out what water you can, remove the wet carpets, remove the baseboards and start removing wet sheetrock. Cut a line about 2 feet up the wall. The straighter you cut, the easier the rebuild will be. Bag debris/insulation etc. and take it outside. Save a square of ruined carpet (2 feet by 2 feet) and ruined carpet pad for the insurance to verify replacement value - if you have multiple carpets, save multiple samples. - Your goal is to get anything wet out of your house so it can begin to dry. Don’t worry about removing glue down hardwoods, let the contractor handle that during the rebuild. </li>

                <li>If you are expecting more rain, <strong>don’t put flood debris where it can float away,</strong> block a drain and cause more trouble. </li>

                <li><strong>Be very careful about hiring “the experts”</strong> - companies will bring in fans, etc. and eat up a lot of your claim check by “drying” your house - once the walls are open, the studs will dry in time. Every dime you spend renting expensive blowers is money you can’t use towards granite countertops or tile upgrades when you rebuild. Fans, your air conditioner a dehumidifier from Home Depot will do the job. You can spray the studs with bleach as they dry out.  If you can't do the work yourself and you do need to hire someone, make sure to confirm with your adjuster what they will cover when it comes to fan's and dehumidifiers....there is a maximum number they will cover per room and per house. If they tell you that they need a generator to start work, confirm how much they will charge you to rent one and make sure to double check if it is covered by your policy and how much they will cover. </li>

                <li><strong>Be careful hiring contractors</strong> - ask for multiple references, ensure they use sub-contractors they know - they will be busy and be prepared to wait. In my opinion it is best to use a local contractor because weeks or months down the road if you have problems, they will still be here to fix it!! </li>

                <li><strong>Plastic storage tubs work better than cardboard boxes</strong> for storage of your undamaged stuff.</li>

                <li><strong>Be nice to the adjustor</strong> - he or she will be valuing your loss and establishing the rebuild - every dollar counts, so be a pleasant memory for the adjustor, rather than “that” person. Understand that some of these adjusters have driven from 4 or 5 states away to come help with this claims process. Ask them as many questions as you can because they are the ones to help you understand what is covered and what is not. (An example would be your cabinets...if the lower cabinets go under water and the upper cabinets do not, they will pay for the lower but not the upper because they were not damaged. You policy pays to only replace the items that were ruined.)</li>

                <li>No matter who your insurance company is, <strong>all flood claims go through the National Flood Insurance Program,</strong> all money comes through FEMA, so the time between the adjustor visiting your house and you getting money takes weeks/months - be patient - it is challenging and horrible waiting, but you are dealing with the government and all the other claims that are in flight as well. Also, know that if you have a mortgage on your home, you will be paid with a two party check and you will have to sign it and send it to your mortgage company. Your mortgage company will send you portions of the dollar amount paid as you make repairs to the structure and you will have to prove that you are repairing the home. </li>
                 
                <li><strong>Your first estimate can be less than you expect,</strong> therefore, you want to work with your contractor to confirm the amount of coverage paid and if it is in line with what they charge. If the original estimate does not satisfy what your contractor charges, make sure your contractor gives you a detailed bid like the proof of loss that you receive from the flood adjuster so you can show in detail why additional claims dollars are needed. Be wary of working with 3rd party arbitrators as they will take a percentage of your total claim, not just any extra they get you in the supplement. </li>

                <li><strong>Accept help when offered and be specific</strong> - if someone asks “what can I do?” tell them something specific - I need candles, contractor bags, sandwiches - be grateful of those that reach out and be honest with what you need. </li>

                <li><strong>You will get through this,</strong> it is a struggle, but you will get through it. Understand that we have become an instant gratification society and things just can't work that way in a catastrophic loss like this! Things will take time!  </li>
              </ol>

