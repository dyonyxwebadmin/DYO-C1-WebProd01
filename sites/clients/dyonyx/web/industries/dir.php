              <p>DIR Contract Number:  <a href="http://www2.dir.state.tx.us/ict/contracts/Pages/Details.aspx?dir_contract_number=DIR-TSO-2595&Return=http%3A//www2.dir.state.tx.us/ict/contracts/Pages/ResultsByContract.aspx%3Fcc%3DDIR-TSO-2595" target="_blank">DIR-TSO-2595</a></p>


              <strong>Infastructure as a Service (IaaS)</strong>
              
              <br /><br />

              The DYONYX High Availability Data Center strategy employs methods which include a combined implementation of physical and virtual equipment in Tier IV data centers (Production in Bryan, Texas and Disaster Recovery in Austin, TX) to provide optimum performance, reduction in physical space, and high availability as common goals. Some of the benefits of the DYONYX data center include:

              <br /><br />

              Military‐grade security with 24x7x365 guards, Biometric & Card Key security for access control and Video Surveillance and Recording;

              <br /><br />

              <ul>
                <li>Reinforced structure including concrete bollards, steel lined walls, and bulletproof glass;</li>
                <li>Fully redundant power including multiple municipal feeds, multiple diesel powered generators and redundant UPS;</li>
                <li>Multiple providers of metropolitan area networks (MANs) and Internet access; and</li>
                <li>100% availability guarantee and bandwidth on demand from 1 Mbps to 1 Gbps;</li>
              </ul>

              High availability will be achieved at the internal network level using high level redundancy. Redundant internaldevices (i.e., power supplies, multiple multi-port network cards; redundant network connections) will be implemented to the network infrastructure to achieve fault tolerant failover of aggregate bandwidth to corporate data and redundant storage devices. Additionally, multiple power distribution units per rack will be connected to separate electrical circuits. Each server’s redundant power supply is connected to independent circuits. This level of redundancy will be implemented for physical and virtual devices.
              
              <br /><br />
              
              DYONYX deploys redundant virtual machine clusters with automated management processes to dynamically move virtual servers in the event of virtual machine or catastrophic host server failure. Additionally, to help attain server and data high availability, DYONYX will leverage imaging, replication (snapshots), cloning technologies and disk-to-disk-to-disk backup/restore environment to invoke rapid deployment/redeployment instant as well as bare metal recovery.
              
